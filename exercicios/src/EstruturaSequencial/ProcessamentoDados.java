package EstruturaSequencial;

public class ProcessamentoDados 
{

	public static void main(String[] args) 
	{
	int a, b;
	a = 5;
	b = 2 * a;
	System.out.print("Exemplo 1: " + a);
	System.out.println(b);
	
	double c, d, e, area;
	c = 6.0 ;
	d = 5.0 ;
	e = 8.0 ;
	area = (c  + d) / 2.0 * e;
	System.out.println("Exemplo 2: " + area);
	
	int f, g;
	double resultado;
	f = 5;
	g = 2;
	resultado = (double) f / g;
	System.out.println("Exemplo 3: " + resultado);
	
	
	}

}
