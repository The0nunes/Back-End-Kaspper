
public class TestaWhile {

	public static void main(String[] args) {
		
		int i = 0; // declarou uma variavel com o noe 'i' e atribuiu o valor '0'.
		
		while(i <= 10) {
			System.out.println(i); // escreveu o valor de 'i' na tela.
			i = i + 3; // incremento.
		}
		System.out.println("Saiu do laço");
		
		
	}

}
